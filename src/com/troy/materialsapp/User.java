package com.troy.materialsapp;

public class User {
	 public String id_biodata, email, nama_lengkap, alamat, no_telpon;
	    
	    public User(String id_biodata, String email, String nama_lengkap, String alamat, String no_telpon) {
	        this.id_biodata = id_biodata;
	        this.email = email;
	        this.nama_lengkap = nama_lengkap;
	        this.alamat = alamat;
	        this.no_telpon = no_telpon;
	    }
}
